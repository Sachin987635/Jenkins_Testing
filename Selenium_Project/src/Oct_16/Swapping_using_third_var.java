package Oct_16;

public class Swapping_using_third_var {
	public static void main(String[] args) {
		int a =10;
		int b =20;
		System.out.println("Value of a "+a);
		System.out.println("Value of b "+b);
		int c=0;
		c=a;
		a=b;
		b=c;
		System.out.println("Swapping done ");
		System.out.println("Value of a "+a);
		System.out.println("Value of b "+b);
		
	}
}
